#include <tim.h>
#include <SysCommDef.h>

TIM_HandleTypeDef htim3;

/*
	统计任务时间节拍器	
//*/
volatile unsigned long long FreeRTOSRunTimeTicks;

 

/* TIM3 init function */
void MX_TIM3_Init(u16 u16period, u16 u16prescaler)
{
  TIM_ClockConfigTypeDef sClockSourceConfig = {0};
  TIM_MasterConfigTypeDef sMasterConfig = {0};

  htim3.Instance = TIM3;
  htim3.Init.Prescaler = u16prescaler;
  htim3.Init.CounterMode = TIM_COUNTERMODE_UP;
  htim3.Init.Period = u16period;
  htim3.Init.ClockDivision = TIM_CLOCKDIVISION_DIV1;
  htim3.Init.AutoReloadPreload = TIM_AUTORELOAD_PRELOAD_ENABLE;
  if (HAL_TIM_Base_Init(&htim3) != HAL_OK)
  {
    logerr("");
  }
  sClockSourceConfig.ClockSource = TIM_CLOCKSOURCE_INTERNAL;
  if (HAL_TIM_ConfigClockSource(&htim3, &sClockSourceConfig) != HAL_OK)
  {
    logerr("");
  }
  sMasterConfig.MasterOutputTrigger = TIM_TRGO_RESET;
  sMasterConfig.MasterSlaveMode = TIM_MASTERSLAVEMODE_DISABLE;
  if (HAL_TIMEx_MasterConfigSynchronization(&htim3, &sMasterConfig) != HAL_OK)
  {
    logerr("");
  }
  HAL_TIM_Base_Start_IT(&htim3);
}



/**
	用TIM3进行计时装载
*/
void ConfigTimerForRunTimeStats(void)
{
	FreeRTOSRunTimeTicks = 0;
	MX_TIM3_Init(50-1 ,90-1);		//周期50us   频率20Khz
}



